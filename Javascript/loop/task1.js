// CountDown Using while loop


{
    let i = 10;

    while (i > 0) {
        console.log(i);
        i--;
    }
}


// CountDown Using do-while loop

{
    let i = 10;
    do{
        console.log(i);
        i--;
    }while(i>0);
}

//Countdown Using for loop

{
    let i;
    for(i=1;i>0;i--)
    {
        console.log(i);
    }
}